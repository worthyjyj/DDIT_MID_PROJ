package vo;

public class FreeReplyVO {
	private int fcom_no;
	private String fcom_content;
	private String fcom_date;
	private int fb_no;
	private String p_name;
	private String p_pic;
	
	public int getFcom_no() {
		return fcom_no;
	}
	public void setFcom_no(int fcom_no) {
		this.fcom_no = fcom_no;
	}
	public String getFcom_content() {
		return fcom_content;
	}
	public void setFcom_content(String fcom_content) {
		this.fcom_content = fcom_content;
	}
	public String getFcom_date() {
		return fcom_date;
	}
	public void setFcom_date(String fcom_date) {
		this.fcom_date = fcom_date;
	}
	public int getFb_no() {
		return fb_no;
	}
	public void setFb_no(int fb_no) {
		this.fb_no = fb_no;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public String getP_pic() {
		return p_pic;
	}
	public void setP_pic(String p_pic) {
		this.p_pic = p_pic;
	}
	
	
	
}
