package vo;

public class NoteVO {
	private int note_no;
	private String note_send;
	private String note_title;
	private String note_content;
	private String note_sdate;
	private String note_edate;
	private int note_status;
	private String mem_id;
	
	public int getNote_no() {
		return note_no;
	}
	public void setNote_no(int note_no) {
		this.note_no = note_no;
	}
	public String getNote_send() {
		return note_send;
	}
	public void setNote_send(String note_send) {
		this.note_send = note_send;
	}
	public String getNote_title() {
		return note_title;
	}
	public void setNote_title(String note_title) {
		this.note_title = note_title;
	}
	public String getNote_content() {
		return note_content;
	}
	public void setNote_content(String note_content) {
		this.note_content = note_content;
	}
	public String getNote_sdate() {
		return note_sdate;
	}
	public void setNote_sdate(String note_sdate) {
		this.note_sdate = note_sdate;
	}
	public String getNote_edate() {
		return note_edate;
	}
	public void setNote_edate(String note_edate) {
		this.note_edate = note_edate;
	}
	public int getNote_status() {
		return note_status;
	}
	public void setNote_status(int note_status) {
		this.note_status = note_status;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}

	
}
